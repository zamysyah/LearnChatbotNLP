# Fungsi klasifikasi intent
def classify_intent(text):
    vec = vectorizer.transform([text])
    return model.predict(vec)[0]

# Fungsi utama untuk mendapatkan respons
def get_response(user_input):
    user_input = user_input.lower()
    intent_tag = classify_intent(user_input)
    print("Predicted intent:", intent_tag)

    # Cari berdasarkan intent yang diprediksi model
    for intent in intents['intents']:
        if intent['tag'] == intent_tag:
            response = random.choice(intent['responses'])
            save_conversation_log(user_input, response)
            return response

    # Jika intent dari model tidak cocok, coba match manual
    for intent in intents['intents']:
        for pattern in intent['patterns']:
            if pattern in user_input:
                response = random.choice(intent['responses'])
                save_conversation_log(user_input, response)
                return response

    # Jika tetap tidak ditemukan, fallback
    fallback_responses = [
        "Hmm... bisa diulangin dengan kata lain?",
        "Saya belum paham itu, tapi saya terus belajar!",
        "Mungkin maksud kamu bisa dijelaskan lebih detail?"
    ]
    response = random.choice(fallback_responses)
    save_conversation_log(user_input, response)
    return response


